    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String nim =txtnim.getText();
        String nama =txtnama.getText();
        String jurusan =(String)cmbjur.getSelectedItem();
        String alamat =txtalamat.getText();
        String jeniskelamin = null;

        if(jRadioButton1.isSelected()){
            jeniskelamin = "Laki-Laki";
        }else if(jRadioButton2.isSelected()){
            jeniskelamin = "Wanita";
        }else{
            JOptionPane.showMessageDialog(rootPane, "tidak boleh kosong");
        }
        try{
            Statement statement =(Statement)conek.GetConnection().createStatement();
            statement.executeUpdate("update tabmahasiswa SET nim = '"+nim+"',nama_mhs='"+nama+"',jurusan='"+jurusan+"',jenis_kelamin='"+jeniskelamin+"',alamat='"+alamat+"'WHERE nim = '"+nim+"';");
            clearform();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

