    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        File reportfile = new File(".");
        String dirr ="";
        try{
            Statement statement =(Statement)conek.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from tabmahasiswa;");
            dirr = reportfile.getCanonicalPath()+"/src/iReportdata/";
            JasperDesign design = JRXmlLoader.load(dirr+"report2.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(design);
            
            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(res);
            JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap(),rsDataSource);
            JasperPrintManager.printReport(jp,true);
        }catch(IOException | SQLException | JRException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

