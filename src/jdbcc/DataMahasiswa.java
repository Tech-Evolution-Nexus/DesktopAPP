    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        try{
            bsimpan.setEnabled(false);
            bhapus.setEnabled(false);
            Statement statement =(Statement)conek.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from tabmahasiswa where nim ='"+txtnim.getText()+"';");
            DefaultTableModel tbl  = new DefaultTableModel();
            tbl.addColumn("NIM");       
            tbl.addColumn("Nama");
            tbl.addColumn("Jurusan");
            tbl.addColumn("Jenis Kelamin");
            tbl.addColumn("Alamat");
            table.setModel(tbl);
            while(res.next()){
                tbl.addRow(new Object[]{
                    res.getString("nim"),         
                    res.getString("nama_mhs"),
                    res.getString("jurusan"),
                    res.getString("jenis_kelamin"),
                    res.getString("alamat")
                });
                table.setModel(tbl);
            }
            
        }catch(Exception e){
             JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_bcariActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
       bsimpan.setEnabled(true);
       bhapus.setEnabled(true);
        int row = table.getSelectedRow();
       DefaultTableModel model = (DefaultTableModel) table.getModel();
       txtnim.setText(model.getValueAt(row,0).toString());
       txtnama.setText(model.getValueAt(row,1).toString());
       cmbjur.getModel().setSelectedItem(model.getValueAt(row,2));
       if(model.getValueAt(row,3).toString().equals("Wanita")){
            jRadioButton2.setSelected(true);   

       }else{
           jRadioButton1.setSelected(true);

       }
       txtalamat.setText(model.getValueAt(row,4).toString());

    }//GEN-LAST:event_tableMouseClicked

    private void txtnimKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnimKeyTyped
        char typedChar = evt.getKeyChar();
        String currentText = txtnim.getText();
        if (currentText.isEmpty() && (Character.isWhitespace(typedChar) || Character.isWhitespace(typedChar) || Character.isISOControl(typedChar))||currentText.length() >= 9 || !Character.isLetterOrDigit(typedChar)) {
        evt.consume();
        }  
    }//GEN-LAST:event_txtnimKeyTyped

